function systemLocaleToSupported(value) {
  const locale = typeof value === "string" ? value.trim().replace(/_/g, "-").toLowerCase() : "";
  if (locale === "zh-cn" || locale === "zh-sg" || locale === "zh-hans" || locale.startsWith("zh-hans-")) return "zh-CN";
  return "en-US";
}

function resolveLocale(preference, systemLocale) {
  const normalized = normalizeLanguagePreference(preference);
  return normalized === "system" ? systemLocaleToSupported(systemLocale) : normalized;
}

function interpolate(template, params = {}) {
  return String(template ?? "").replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => {
    const value = params && Object.prototype.hasOwnProperty.call(params, key) ? params[key] : undefined;
    return value === undefined || value === null ? match : String(value);
  });
}

function translate(locale, key, params) {
  const resolved = SUPPORTED_LOCALES.includes(locale) ? locale : "en-US";
  const template = CATALOGS[resolved][key] ?? CATALOGS["en-US"][key] ?? key;
  return interpolate(template, params);
}

function catalogDifferences() {
  const english = Object.keys(EN_US).sort();
  const chinese = Object.keys(ZH_CN).sort();
  return {
    missingEnglish: chinese.filter(key => !Object.prototype.hasOwnProperty.call(EN_US, key)),
    missingChinese: english.filter(key => !Object.prototype.hasOwnProperty.call(ZH_CN, key)),
  };
}

function createLocalizer(options = {}) {
  let preference = normalizeLanguagePreference(options.preference);
  let systemLocale = typeof options.systemLocale === "string" ? options.systemLocale : "en-US";
  return {
    get preference() { return preference; },
    get locale() { return resolveLocale(preference, systemLocale); },
    setPreference(value) { preference = normalizeLanguagePreference(value); return this.locale; },
    setSystemLocale(value) { systemLocale = typeof value === "string" ? value : systemLocale; return this.locale; },
    t(key, params) { return translate(this.locale, key, params); },
    rendererStrings() {
      return Object.fromEntries(RENDERER_KEYS.map(key => [key, translate(this.locale, `renderer.${key}`)]));
    },
  };
}

function translateReason(localizer, reason) {
  if (typeof reason !== "string" || !reason) return localizer.t("fallback.unknownError");
  const key = `reason.${reason}`;
  return Object.prototype.hasOwnProperty.call(CATALOGS[localizer.locale], key) ? localizer.t(key) : reason.slice(0, 240);
}

module.exports = {
  CATALOGS,
  LANGUAGE_PREFERENCES,
  RENDERER_KEYS,
  SUPPORTED_LOCALES,
  catalogDifferences,
  createLocalizer,
  interpolate,
  normalizeLanguagePreference,
  resolveLocale,
  systemLocaleToSupported,
  translate,
  translateReason,
};
